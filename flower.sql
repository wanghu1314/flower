/**订单表**/
CREATE TABLE flower_order(
  oid INT PRIMARY KEY AUTO_INCREMENT,
  uid INT,
  fid INT,                #鲜花编号
  count INT,              #购买数量
  oprice DECIMAL(10,2),   #总价
