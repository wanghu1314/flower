SET NAMES UTF8;
DROP DATABASE IF EXISTS flower;
CREATE DATABASE flower CHARSET=UTF8;
USE flower;


/*******鲜花类别*******/
CREATE TABLE flower_sort(
  sid INT PRIMARY KEY AUTO_INCREMENT, 
  sname VARCHAR(32)  #类别
);

/**鲜花表**/
CREATE TABLE flower(
  fid INT PRIMARY KEY AUTO_INCREMENT, #鲜花编号
  sid INT,                            #鲜花分类编号
  fname VARCHAR(32),                  #鲜花名称
  title VARCHAR(32),                  #鲜花标题
  price DECIMAL(10,2),                #价格
  material VARCHAR(60),               #材料
  pack  VARCHAR(60),                  #包装
  flower_language  VARCHAR(40),       #花语  
  details VARCHAR(60)                 #产品说明
     
);

/**鲜花图片**/
CREATE TABLE flower_pic(
  pid INT PRIMARY KEY AUTO_INCREMENT, #鲜花图片编号
  fid INT,                    #鲜花编号
  img VARCHAR(128),         #图片路径
  img_1 VARCHAR(128), 
  img_2 VARCHAR(128),
  img_3 VARCHAR(128)
);
/**用户信息**/
CREATE TABLE flower_user(
  uid INT PRIMARY KEY AUTO_INCREMENT,
  uname VARCHAR(32),
  upwd VARCHAR(32),
  email VARCHAR(64),
  phone VARCHAR(16),

  avatar VARCHAR(128),        #头像图片路径
  user_name VARCHAR(32),      #用户名，如王小明
  gender INT                  #性别  0-女  1-男
);

/**收货地址信息**/
CREATE TABLE flower_address(
  aid INT PRIMARY KEY AUTO_INCREMENT,
  uid INT,                    #用户编号
  receiver VARCHAR(16),       #接收人姓名
  province VARCHAR(16),       #省
  city VARCHAR(16),           #市
  county VARCHAR(16),         #县
  address VARCHAR(128),       #详细地址
  cellphone VARCHAR(16),      #手机
  fixedphone VARCHAR(16),     #固定电话
  postcode CHAR(6),           #邮编
  tag VARCHAR(16),            #标签名

  is_default BOOLEAN          #是否为当前用户的默认收货地址
);

/**购物车**/
CREATE TABLE flower_cart(
  cid INT PRIMARY KEY AUTO_INCREMENT,
  uid INT,            #用户编号
  fid INT,            #鲜花编号
  title VARCHAR(32),  #鲜花名称
  count INT,          #购买数量
  price DECIMAL(10,2),
  img_url varchar(25) #图片路径
  
);
